<script>
    document.addEventListener("DOMContentLoaded", function () {
      const projectCards = document.querySelectorAll(".project-card");
      const specTitles = document.querySelectorAll(".spec-title");
      const sectionTitles = document.querySelectorAll(".port-section-title");
  
      function checkVisibility() {
        projectCards.forEach((card) => {
          const cardTop = card.getBoundingClientRect().top;
          const cardBottom = card.getBoundingClientRect().bottom;
          const windowHeight = window.innerHeight;
  
          if (cardTop < windowHeight - 100 && cardBottom > 0) {
            card.classList.add("visible");
          } else {
            card.classList.remove("visible");
          }
        });
  
        specTitles.forEach((title) => {
          const titleTop = title.getBoundingClientRect().top;
          const titleBottom = title.getBoundingClientRect().bottom;
          const windowHeight = window.innerHeight;
  
          if (titleTop < windowHeight - 100 && titleBottom > 0) {
            title.classList.add("visible");
          } else {
            title.classList.remove("visible");
          }
        });
  
        sectionTitles.forEach((title) => {
          const titleTop = title.getBoundingClientRect().top;
          const titleBottom = title.getBoundingClientRect().bottom;
          const windowHeight = window.innerHeight;
  
          if (titleTop < windowHeight - 100 && titleBottom > 0) {
            title.classList.add("visible");
          } else {
            title.classList.remove("visible");
          }
        });
      }
  
      window.addEventListener("scroll", checkVisibility);
      checkVisibility(); // Initial check when the page loads
    });
  </script>
  
  
  
